<template>
  <div>
    <div class="sm:hidden">
      <label for="tabs" class="sr-only">Select a tab</label>
      <select
        id="tabs"
        name="tabs"
        class="
          block
          w-full
          focus:ring-indigo-500
          focus:border-indigo-500
          border-gray-300
          rounded-md
        "
      >
        <option v-for="tab in tabs" :key="tab.name" :selected="tab.current">
          {{ tab.name }}
        </option>
      </select>
    </div>
    <div class="hidden sm:block">
      <nav
        class="relative z-0 rounded-lg shadow flex divide-x divide-gray-200"
        aria-label="Tabs"
      >
        <button
          v-for="(tab, tabIdx) in tabs"
          :key="tab.name"
          @click="store.setView(tab.view)"
          :class="[
            store.state.view === tab.view
              ? 'bg-white text-gray-900'
              : 'text-gray-500 hover:text-gray-700 bg-gray-200 border-separate',
            tabIdx === 0 ? 'rounded-l-lg' : '',
            tabIdx === tabs.length - 1 ? 'rounded-r-lg' : '',
            'group relative min-w-0 flex-1 overflow-hidden py-2 px-2 text-sm font-medium text-center hover:bg-gray-50 focus:z-10',
          ]"
          :aria-current="store.state.view === tab.view ? 'page' : undefined"
        >
          <span>{{ tab.name }}</span>
        </button>
      </nav>
    </div>
  </div>
</template>

<script setup>
import store from "../store.js";

const tabs = [
  { name: "Color", view: "color" },
  { name: "Black and White", view: "bw" },
  { name: "1-Bit", view: "onebit" },
];
</script>